package com.mam558;

public class FibonacciMessage {
    static public class Result {
        public final int n;

        public Result( int n) {
            this.n = n;
        }
    }

    static public class FirstResult {
        public final int n;

        public FirstResult( int n) {
            this.n = n;
        }
    }
    static public class SecondResult {
        public final int n;

        public SecondResult( int n) {
            this.n = n;
        }
    }

    static public class FinalResult {
        public final int n;

        public FinalResult(int n) {
            this.n = n;
        }
    }
}
